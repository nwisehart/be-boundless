              <h2 class="mixed"><span>Making smart</span> touch a reality</h2>
              
              <p class="callout">With real-life users</p>

              <p>Two years ago, at the start of the project, Mott lugged a huge, table-sized Microsoft Surface into Provail: a local center that helps people with disabilities pursue the lives they want to live. Among the nonprofit’s many offerings? Computer classes — and lots of motivated learners willing to participate in his study. </p>

              <p>There, Mott worked with eight volunteers to make Smart Touch a reality, beginning by asking them to hit targets on the screen. “I had a wide range of participants, so I saw a wide range of strategies used to complete the tasks,” he says. The first person would prop one hand on the tablet for support while dragging the other hand across the screen. The next person would interact with the device in an entirely different way. This, says Mott, is why inclusive innovation — actually looping the software’s target population into the design process — is so important. </p>

              <p>“When people are motivated to use a technology, they’ll employ a lot of different strategies in order for that to become possible. It was difficult to get an understanding of what ‘touch’ looks like,” he says. “People used the software in ways I couldn’t have predicted, which resulted in a lot of failure — and fixing — for me.” </p>


