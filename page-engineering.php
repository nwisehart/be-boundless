<p>The participants in the CSNE’s studies are epilepsy patients who have volunteered to assist the CSNE with its research (ECoG is the go-to approach for mapping the origin of seizures) during their one- to two- week hospital stay. There, CSNE researchers supervised by Dr. Ojemann connect the signals measured directly from the brain to a computer translator of sorts and, without physically lifting a finger, the participants practice thinking of controlling a cursor on a screen, an avatar hand, or even a robotic arm. The researchers ask the participant to imagine completing a task — grabbing that cup of coffee, perhaps — then analyze the signals as the participant controls the electronic limb with their brain. </p>
<p>“Now that we know we can extract activity from the brain and control avatar hands and robotic arms, what happens if we want to control our own paralyzed limb?” poses Moritz. “We can electrically stimulate the spinal cord below the level of the injury using an implantable device, a type of brain-computer interface (BCI).” </p>
<p>The concept is this: the brain activity is recorded, decoded and an algorithm is created. That information is then used to program the implantable BCI — essentially, chips and wires that acts as a pseudo nervous system, creating a bridge to reconnect the brain to the spinal cord. </p>

<p class="gold-quote headline-gold">The device listens to the brain, and then says ‘Oh, the person wants to do this — let’s help them,’” says Moritz. </p>

<p>“We’re trying to make the algorithms smarter, to allow the device learn in real time. Just as our brain or spinal cord would learn with practice, so would this little implant.”</p>
<p>Another forward-thinking goal at the core of the CSNE is not to just stop at BCI-driven reanimation, but actually help brain and spinal circuits to heal themselves, bringing dormant limbs to life. </p>
<p>“CSNE researchers are leading the way in building bi-directional BCIs that allow two-way communication between groups of neurons. These devices can promote new or stronger connections in the brain, encouraging the nervous system to rewire around an area of injury,” says Rao. “The goal isn’t so much to use a brain signal to control a prosthetic limb as it is to allow the brain to heal itself.”</p>
<p>For Jayna and other survivors of brain injury, a device like this could mean improving or restoring motor function. For those with spinal cord injury and paralysis, it could even mean having the ability to move their own bodies again. </p>
<p>“Eventually, the device could go away because the individual has regained function,” says Moritz. “The individuals, like Jayna, who might one day benefit from what the CSNE is developing are just a wonderful, inspiring, rewarding group of people to work with. This is the kind of research that makes being at the UW so special.” </p>


