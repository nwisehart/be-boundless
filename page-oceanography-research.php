                                <h2 class="heading3">Lessons from the microscopic</h2>
                                <p class="para3-1">Back on the RV Carson, Crouser and Seroy are labeling containers full of specimens that were just hauled up from different depths.</p>

                                <p class="para3-2">One contains a thick soup of tiny krill and zooplankton. Another sample, from shallower water, is sparsely populated. It&rsquo;s a vertical snapshot of a mass migration: Zooplankton surface at night to eat phytoplankton, then head back to deeper, darker water to avoid being seen by predators in the daylight.</p>

                                <p class="para3-3">Across a narrow passageway, Gr&#252;nbaum is working at a table covered in wires, housings, infrared cameras and microcontrollers (small computers). He holds a microcontroller that will soon be attached to a drifter. &ldquo;The technology is making this research easier, and the engineering is getting better and better,&rdquo; he says.</p>

                                <p class="para3-4">That&rsquo;s good, because in addition to tracking the movements of large zooplankton populations, he and Keister are observing individual zooplankton in their natural habitat with the help of infrared video cameras.</p>

                                <p class="para3-5">It&rsquo;s hard to fathom that the behavior of a single microscopic creature might give us insight into the future of the Puget Sound and our oceans. But Keister and Gr&#252;nbaum suspect that zooplankton&rsquo;s behavior in response to changing environmental conditions may be magnified as they ripple up through the ecosystem.</p>

                                <p class="para3-6">&ldquo;The water in Hood Canal is stratified,&rdquo; explains Keister, meaning that different depths have widely divergent oxygen and pH levels. &ldquo;Zooplankton are moving through big differences in conditions as they go up and down.&rdquo;</p>

                                <p class="para3-7">Gr&#252;nbaum gives an example of what these movements can teach us: If significant populations of zooplankton are able to hide in low-oxygen waters to avoid predators, we may see a drop in salmon populations and an uptick in jellyfish &mdash; predators that are better suited to those conditions.</p>

                                <p class="para3-8">Whatever the findings, says Keister, &ldquo;It could have significant implications for the food web.&rdquo; This includes humans: where we harvest animals, and what we are (or are not) able to catch.</p>

