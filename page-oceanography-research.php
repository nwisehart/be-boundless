                                <h2 class="heading1">No longer adrift</h2>
                                <p class="para1">A year ago, Crouser would never have imagined herself filming marine animals in the dark. At the time, she was a chemical engineering major struggling to figure out how to apply her education to helping the environment, a cause she had always believed in. Then she took an introduction to oceanography course.</p>

                                <p class="para2">&ldquo;It felt like I was in a TED Talk every time I went to class,&rdquo; says Crouser, now a senior. &ldquo;The lab is very hands-on. You have to do lots of experiments, and then you go on a day trip on the RV Carson. Once I did that, I was all in.&rdquo;</p>

                                <p class="para3">After changing her major to oceanography, Crouser landed a summer internship, funded by the National Science Foundation, that culminated in this research cruise. For several weeks beforehand, she spent time in the lab of oceanography professor Julie Keister, helping investigate the relationship between local water conditions and zooplankton populations.</p>

                                <p class="para4">It&rsquo;s been a steep learning curve, but Crouser embraces it. &ldquo;There’s always something to look forward to,&rdquo; she says. &ldquo;I feel like this internship gave me life again.&rdquo;</p>

                                <p class="para5">Another aspect of her Husky Experience also helps Crouser feel hopeful about the future: her commitment to encouraging other women and people of color to explore STEM careers. She&rsquo;s the outreach director for WChE (Women in Chemical Engineering) and the webmaster and historian for SACNAS (Society for Advancement of Chicanos/Hispanics and Native Americans in Science). Much of her involvement comes from wanting to show people who look like her that they can be scientists, too.</p>

                                <p class="para6">&ldquo;Even if I don’t know exactly what I’m doing, I still need to show up and show other people that they can be where I&rsquo;m standing,&rdquo; she says.</p>

                                <h2 class="heading1-2">Tiny animals, big impact</h2>
                                <p class="para7">Crouser admits that when she started her internship, &ldquo;I didn&rsquo;t know the name of one kind of zooplankton. I didn&rsquo;t even know where they were in the food web.&rdquo; Now she grasps their immense importance.</p>

                                <p class="para8">Zooplankton spend their lives evading predators and searching for food &mdash; usually phytoplankton, the microscopic algae that are the linchpin of all ocean life. Where phytoplankton bloom, so do zooplankton; and where zooplankton thrive, so do larger marine animals, from salmon to orca whales.</p>

                                <p class="para9">What will happen to zooplankton as our oceans continue to warm and absorb human-produced carbon dioxide, growing more acidic and hypoxic (low in oxygen)? And if they adjust their behavior because of these factors, what happens to the animals that feed on them?</p>

                                <p class="para10">These are big questions with big ramifications. To help peer into the future, Keister and fellow UW School of Oceanography Professor Daniel Gr&#252;nbaum are starting very, very small.</p>

