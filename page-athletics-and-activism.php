<p>On Sept. 23, 2016 — minutes before the Husky women’s soccer team kicked off its season conference opener — McElvaine dropped a bare knee on the damp playing field of Husky Soccer Stadium as “The Star-Spangled Banner” rang through the loudspeakers. </p>
<p>The moment was inspired by San Francisco 49ers then-quarterback Colin Kaepernick, who had opted one month earlier not to stand during the pregame national anthem. Instead he sat on the bench, arms crossed. It was a quiet response to a polarizing issue: police brutality against people of color. </p>
<p>A movement took hold as athletes across the country sat, kneeled, raised fists and linked arms for their own causes, sparking a nationwide conversation about human rights. </p>
<p>The day before she took a knee, McElvaine had watched footage of the shooting of Terence Crutcher, a black man killed by a police officer in Tulsa, Oklahoma. “I’d seen a bunch of videos before,” she says, “but with this one, I just couldn’t get myself together. I couldn’t control my crying.”</p>
<p>At practice, McElvaine powered through her drills, but her coaches and teammates knew something was wrong. </p>
<p>“The next day, before the national anthem, Coach Gallimore looked at me and said, ‘Havana, if you want to kneel, you can,’” recalls McElvaine. “I hadn’t even thought about kneeling, but in that moment, she really knew me better than I knew myself. She enabled me to be in solidarity with a movement I cared about, and that was really powerful for me.” </p>


