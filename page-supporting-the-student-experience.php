                  <p><b>I grew up in Colorado in the 1990s and 2000s, when there was a huge pine beetle outbreak — they killed all the trees.</b> My family and I would drive into the mountains and everything would be brown, and I thought, “Hey, I want to do something to fix that.” </p>
                  <p><b>I took wildland fire management with professor Ernesto Alvarado last year.</b> Forest fires are a popular topic within forestry, but I didn’t have an interest until I took his course. Now I’m an undergraduate research assistant in his Wildland Fire Sciences Lab, and even joined him last summer to do LIDAR at the King Fire in Northern California. LIDAR is a cool tool that uses laser technology to create a 3D map of the forest to give us more accurate information much, much quicker — it’s the latest and greatest in our field. </p>
                  <p><b>I'm interested in sustainable forest management, and my research looks at how forest fuels change after post-fire salvage logging.</b> Salvage logging is a contentious subject, and our aim is to add some objective data to the debate. If the effects of salvage logging on fuels can be quantified and predicted, forest managers can make more informed decisions on how to sustainably manage their forests after disturbances.</p>
                  <p><b>I joined the U.S. Navy right out of high school, providing patient care and working in purchasing as a hospital corpsman in Guantanamo Bay, Cuba for three-and-a-half of my five years in the service.</b> The Navy instilled a lot of good traits in me — a strong work ethic, integrity, and a mindfulness and appreciation for financial stewardship. An education is a privilege, and it means a lot to me that donors have entrusted me with their resources so I can focus on my courses. </p>
                  <p><b>I came to Seattle for the University of Washington — it’s one of the only universities with a program like this.</b> After graduation, I want to go to graduate school at the UW to get a master of forest resources, then work as a forest researcher before moving into forest policy. </p>                              


