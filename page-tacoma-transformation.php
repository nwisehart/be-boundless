<p>Rochat was supposed to be a construction worker. </p>
<p>He’d always found solace in working with his hands, so after the Poulsbo, Washington native completed his tours of duty as a paratrooper in the Army, the decision to go to Olympic College for welding technology was an easy one. He earned his associate degree, with plans to hop on a construction site — then the economy tanked. “I was ready with my tool bag and my welding hat, but suddenly there were no more jobs,” he says. </p>
<p>So he went to work in apartment maintenance instead, painting walls, unclogging toilets and, in a cruel twist of fate, upgrading the downspouts on a four-story building’s shiny metal roof. He slipped, fell and crushed his knees, already beat from years spent jumping out of airplanes. “I was literally on crutches trying to hold up sheets of drywall and patch roofs so I could put food on the table,” says Rochat. “But I was too injured to work and didn’t have health insurance, so I got laid off.” </p>
<p>His $12,000 annual salary — the salary that supported his family — was slipping. He needed a new job. One that would keep him off his feet. </p>
<p>Enter Office Depot, or maybe Staples. </p>
<p>“I decided maybe I could be a tier one help desk guy,” says Rochat. “The guy you call when your device is broken who says, ‘Turn it on and off again.’ I’ll be that guy.” </p>


<iframe width="1280" height="720" src="https://www.youtube.com/embed/QI50yL8rAIU?rel=0&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>


<p>It made sense: Rochat had loved computers since he was a kid. When he was 6, his father convinced his mother to buy one for the family. By 8, Rochat was writing his first program thanks to hours spent poring over books with his dad. At 15, he traded teaching a company how he’d hacked them in exchange for dodging prosecution. </p>
<p>“I had no idea I had all this computing power,” says Rochat. But he knew computers made him happy, and he knew he needed a job. So he went back to Olympic College and earned a two-year computer science degree in nine months, armed to join an office supply company’s customer service team. </p>
<p>His instructor, says Rochat, had other plans. “He looked at me and said, ‘You’re writing papers for me on chipset compliance for wireless security. You can do so much more.’”</p>
<p>With that encouragement, Rochat applied to UW Tacoma. “In a million years, I never thought I’d be accepted. UW Tacoma was the pinnacle,” he says. “Two weeks later, I was sitting in my first class. I didn’t even know how I got there.” </p>


